package SelfPrac;

public class SwapTwoNumber {
	
	public static void main(String[] args)
	
	{
		int fno = 10;
		int sno = 20;
		fno = fno + sno;
		sno = fno-sno;
		fno = fno-sno;
		
		System.out.println("value of first number: " + fno);
		System.out.println("value of 2nd number: " + sno);
		
	}

}
